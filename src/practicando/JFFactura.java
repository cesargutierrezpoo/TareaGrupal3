    private void jTFCantidadFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTFCantidadFocusLost
        // TODO add your handling code here:
        
        if(!"".equals(this.jTFCantidad.getText())){
            int cant;
            double preUnit, total;

            cant = Integer.parseInt(this.jTFCantidad.getText());
            preUnit = Double.parseDouble(jTFPreUnit.getText());
            
            total = miCalculo.calcularTotal(cant, preUnit);
            
            this.jTFSubtotal.setText(String.valueOf(total));
            
        }else{
            this.jTFSubtotal.setText("");
        }
    }//GEN-LAST:event_jTFCantidadFocusLost


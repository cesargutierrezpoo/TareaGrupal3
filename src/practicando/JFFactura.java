    private void llenarTabla(){
        
        String dato[] = new String[5];
        dato[0] = jTFCodProducto.getText();
        dato[1] = jTFDescripcionProducto.getText();
        dato[2] = this.jTFCantidad.getText();
        dato[3] = jTFPreUnit.getText();
        dato[4] = this.jTFSubtotal.getText();
        
        modelo.addRow(dato);
        
        totalizar();
    }
    
    
    private void seleccionarFila(){
        String codProducto, descripcion, cantidad, preUnit, total;
        
        if(this.jTblFactura.getSelectedRow() != -1){
            
            filaSeleccionada = this.jTblFactura.getSelectedRow();
            
            codProducto = this.jTblFactura.getValueAt(filaSeleccionada, 0).toString();
            descripcion = this.jTblFactura.getValueAt(filaSeleccionada, 1).toString();
            cantidad = this.jTblFactura.getValueAt(filaSeleccionada, 2).toString();
            preUnit = this.jTblFactura.getValueAt(filaSeleccionada, 3).toString();
            total = this.jTblFactura.getValueAt(filaSeleccionada, 4).toString();
            
            jTFCodProducto.setText(codProducto);
            jTFDescripcionProducto.setText(descripcion);
            this.jTFCantidad.setText(cantidad);
            jTFPreUnit.setText(preUnit);
            this.jTFSubtotal.setText(total);
            
        }
        
    }
    
    private double sumarTotal(){
            
        int numFila = modelo.getRowCount();
        
        double sumarTotal = 0;
        
        for(int i = 0; i < numFila; i++){
            sumarTotal += Double.parseDouble(modelo.getValueAt(i, 4).toString());
        }
        
        return sumarTotal;
    }
    
    private void totalizar(){
        
        // Ir acumulando y mostrando el valor del subtotal sumado.
        
        double totalSumado, impuesto, totalPago;

        totalSumado = sumarTotal();
        impuesto = miCalculo.calcularImpuesto(totalSumado);
        totalPago = miCalculo.calcultarTotalPago(totalSumado,impuesto);
        
        this.jTFSubtotalSumado.setText(String.valueOf(totalSumado));
        this.jTFImpuesto.setText(String.valueOf(impuesto));
        this.jTFTotalPago.setText(String.valueOf(totalPago));
    }
    
     private void eliminarFilaProducto(){

        filaSeleccionada = this.jTblFactura.getSelectedRow();
        
        if(filaSeleccionada != -1){
            modelo.removeRow(filaSeleccionada);
        }else{
            JOptionPane.showMessageDialog(null, "Seleccione una fila");
        }
    }
     
     private void editarFilaProducto(){
      
        String dato[] = new String[5];
        dato[0] = jTFCodProducto.getText();
        dato[1] = jTFDescripcionProducto.getText();
        dato[2] = this.jTFCantidad.getText();
        dato[3] = jTFPreUnit.getText();
        dato[4] = this.jTFSubtotal.getText();
        
        int filas;
        filas = modelo.getColumnCount();
        for(int i = 0; i < filas; i++){
            modelo.setValueAt(dato[i], filaSeleccionada, i);
        }
        
    }
     
    private void limpiarDatosProducto(){
        jTFCodProducto.setText("");
        jTFDescripcionProducto.setText("");
        this.jTFCantidad.setText("");
        jTFPreUnit.setText("");
        this.jTFSubtotal.setText("");
        
    }

private void nuevaFactura(){
        this.jTFNumFactura.setText("");
        this.jTFFecha.setText("");
        this.jCboTipoFactura.removeAllItems();
        jTFCodCliente.setText("");
        jTFNombreCliente.setText("");
        jTFDireccion.setText("");
        
        limpiarDatosProducto();
        
        int filas;
        filas = modelo.getRowCount();
        
        for(int i = 0; i < filas; i++){
            modelo.removeRow(0);
        }
        
        llenarComboTipoFactura();
        
        this.jTFSubtotalSumado.setText("");
        this.jTFImpuesto.setText("");
        this.jTFTotalPago.setText("");
        
        this.jTFNumFactura.requestFocus();
    }    
    
